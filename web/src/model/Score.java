package model;

public class Score {
	private String cid;
	private String sid;
	private String score;
	public String getCid(){
		return cid;
	}
	public void setCid(String cid){
		this.cid = cid;
	}
	public String getSid(){
		return sid;
	}
	public void setSid(String sid){
		this.sid = sid;
	}
	public String getScore(){
		return score;
	}
	public void setScore(String score){
		this.score = score;
	}
}
