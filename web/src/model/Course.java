package model;

public class Course {
	private String cid;
	private String cname;
	private String point;
	public String getCid(){
		return cid;
	}
	public void setCid(String cid){
		this.cid = cid;
	}
	public String getCname(){
		return cname;
	}
	public void setCname(String cname){
		this.cname = cname;
	}
	public String getPoint(){
		return point;
	}
	public void setPoint(String point){
		this.point = point;
	}
}
